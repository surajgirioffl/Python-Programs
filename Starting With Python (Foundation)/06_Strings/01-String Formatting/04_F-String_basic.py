"""
    Strong formatting using most popular and most used format 'f-string'.
"""
# it's simple just put 'f' before the string and inside curly braces write the variable name that you want to replace with it's value.
name = "Suraj Giri"
roll = 10
subject = "Python"
topic = "String Formatting"
print(f"Name: {name} && Roll: {roll} && Subject: {subject} && Topic: {topic}")
# Output: Name: Suraj Giri && Roll: 10 && Subject: Python && Topic: String Formatting
